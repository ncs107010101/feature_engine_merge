import pandas as pd
import numpy as np
from ...core import BaseFeature, register_feature
from ...utils import zscore_rolling, ewm_smooth, ts_rank_center

@register_feature
class FDbPeakVelocityRatio(BaseFeature):
    name = "f_db_peak_velocity_ratio"
    description = "Tick feature: f_db_peak_velocity_ratio"
    required_columns = ["StockId", "Date", "raw_db_peak_vel_ratio"]
    data_combination = "single_stock_tick"

    def calculate(self, data: pd.DataFrame, **kwargs) -> pd.DataFrame:
        df = data.copy()
        g = df.groupby("StockId", group_keys=False)


        out_series = df["raw_db_peak_vel_ratio"].copy()
        
        # Post-processing
        out = out_series
        out = out.replace([np.inf, -np.inf], np.nan).fillna(0.0)

        
        result = pd.DataFrame({
            "StockId": df["StockId"],
            "Date": df["Date"],
            self.name: out
        })
        return result
