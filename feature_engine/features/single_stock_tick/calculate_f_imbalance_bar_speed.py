import pandas as pd
import numpy as np
from ...core import BaseFeature, register_feature
from ...utils import zscore_rolling, ewm_smooth, ts_rank_center

@register_feature
class FImbalanceBarSpeed(BaseFeature):
    name = "f_imbalance_bar_speed"
    description = "Tick feature: f_imbalance_bar_speed"
    required_columns = ["StockId", "Date"]
    data_combination = "single_stock_tick"

    def calculate(self, data: pd.DataFrame, **kwargs) -> pd.DataFrame:
        df = data.copy()
        g = df.groupby("StockId", group_keys=False)


        out_series = g["raw_order_imbalance"].transform(lambda x: x.fillna(0).rolling(20, min_periods=5).apply(lambda w: pd.Series(w).rank(pct=True).iloc[-1]))
        
        # Post-processing
        out = out_series
        out = out.replace([np.inf, -np.inf], np.nan).fillna(0.0)

        
        result = pd.DataFrame({
            "StockId": df["StockId"],
            "Date": df["Date"],
            self.name: out
        })
        return result
